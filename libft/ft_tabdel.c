#include "includes/libft.h"

void			ft_tabdel(char ***tab)
{
	int			i;

	i = 0;
	while (tab[0][i] != 0)
	{
		free(tab[0][i]);
		i++;
	}
	free(tab[0]);
	*tab = NULL;
}
